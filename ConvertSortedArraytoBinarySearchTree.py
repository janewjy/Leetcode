class Solution(object):
    def sortedArrayToBST(self, nums):
        """
        :type nums: List[int]
        :rtype: TreeNode
        """
        if not nums:
            return
        i,j = 0 ,len(nums)-1
        mid = (i+j)/2
        root = TreeNode(nums[mid])
        root.left = self.sortedArrayToBST(nums[i:mid])
        root.right = self.sortedArrayToBST(nums[mid+1:j+1])
        return root
        
        